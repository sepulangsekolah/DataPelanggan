    private void bsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpanActionPerformed

        try {
            Connection con = conek.getkoneksi();
            PreparedStatement stmt = con.prepareStatement("insert into pelanggan (id_pelanggan, nama, jeniskelamin, alamat) values(?,?,?,?)");
            stmt.setString(1, txtid.getText());
            stmt.setString(2, txtnama.getText());
            stmt.setString(3, cmbjk.getSelectedItem().toString());
            stmt.setString(4, txtalamat.getText());
            stmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
        } catch (Exception t) {
            JOptionPane.showMessageDialog(null, "Gagal menyimpan data");
        }
        
        datatable();
    }//GEN-LAST:event_bsimpanActionPerformed

    private void bhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bhapusActionPerformed

        String id_pelanggan = txtid.getText();
        
        try {
            Statement statement = (Statement) conek.getkoneksi().createStatement();
            statement.executeUpdate("DELETE from pelanggan where id_pelanggan=('" + id_pelanggan + "');");
            JOptionPane.showMessageDialog(null, "Data berhasil dihapus");
            txtid.setText("");
            txtnama.setText("");
            cmbjk.setSelectedItem("");
            String alamat = null;
            txtalamat.setText("");
            txtid.requestFocus();
        } catch (Exception t) {
            JOptionPane.showMessageDialog(null, "Gagal menghapus data");
        }
        datatable();
    }//GEN-LAST:event_bhapusActionPerformed

