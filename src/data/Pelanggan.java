    public void datatable() {
        String[] kolom = {"ID Pelanggan", "Nama ", "Jenis Kelamin ", "Alamat"};
        
        DefaultTableModel tbl = new DefaultTableModel(null, kolom);
        try {
            Statement stmt = (Statement) conek.getkoneksi().createStatement();
            String  query = "SELECT * FROM pelanggan";
            ResultSet rs = stmt.executeQuery(query);
            while (rs.next()) {
                String id = rs.getString("id_pelanggan");
                String nama = rs.getString("nama");
                String jenkelamin = rs.getString("jeniskelamin");
                String alamat = rs.getString("alamat");
                
                tbl.addRow(new String[]{id, nama, jenkelamin, alamat});
            }
        }   catch (SQLException ex) {
            ex.printStackTrace();
        }
        tabel.setModel(tbl);
    }
