    private void bcariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bcariActionPerformed
        try {
            Statement statement = (Statement) conek.getkoneksi().createStatement();
            ResultSet res = statement.executeQuery("select * from pelanggan where "
                    + "id_pelanggan='" + txtcari.getText() + "'");
            DefaultTableModel tbl = new DefaultTableModel();
            tbl.addColumn("id_pelanggan");
            tbl.addColumn("nama");
            tbl.addColumn("alamat");
            
            tabel.setModel(tbl);
            
            while (res.next()) {
                tbl.addRow(new Object[]{
                            res.getString("id_pelanggan"),
                            res.getString("nama"),
                            res.getString("alamat")
                        });
                tabel.setModel(tbl);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Masukan ID");
             datatable();
        }
    }//GEN-LAST:event_bcariActionPerformed

